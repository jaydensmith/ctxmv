@testable import CTXMVKit
import Foundation
import Testing

/// Shared raw XML-ish payloads used to exercise Claude Code content decoding heuristics.
private enum DecoderFixtures {
    static let commandFull = """
    <command-message>skill:example</command-message>
    <command-name>/skill:example</command-name>
    <command-args>create plan for destination A</command-args>
    """

    static let commandNameOnly = """
    <command-name>/skill:foo</command-name>
    """

    static let caveat = """
    <local-command-caveat>Caveat: The messages below were generated by the user while running local \
    commands.</local-command-caveat>
    """

    static let stdoutSingle = "<local-command-stdout>Hello world\nExit code: 0</local-command-stdout>"
    static let stdoutMulti = "<local-command-stdout>multi\nline\noutput</local-command-stdout>"

    static let taskNotificationFull = """
    <task-notification>
    <task-id>abc123</task-id>
    <summary>Agent "Route calculation" completed</summary>
    <result>13 segments processed.</result>
    </task-notification>
    """

    static let taskNotificationSummaryOnly = "<task-notification><summary>Done</summary></task-notification>"
    static let taskNotificationEmpty = "<task-notification></task-notification>"

    static let unchangedGenerics = "Hello <String> and <T> Swift generics"
    static let unchangedCode = "func foo<T: Equatable>(_ x: T) {}"
    static let unchangedHtml = "<div class=\"foo\">HTML</div>"
    static let unchangedPlain = "Plain text with no tags"
}

struct ClaudeCodeCommandTests {
    struct CommandCase: CustomTestStringConvertible {
        let input: String
        let expectedContains: [String]
        let expectedExcludes: [String]

        static let allCases: [CommandCase] = [
            CommandCase(
                input: DecoderFixtures.commandFull,
                expectedContains: ["[Command]", "/skill:example", "create plan for destination A"],
                expectedExcludes: ["<command-name>", "<command-args>"]
            ),
            CommandCase(
                input: DecoderFixtures.commandNameOnly,
                expectedContains: ["[Command]", "/skill:foo"],
                expectedExcludes: ["<command-name>"]
            ),
        ]

        var testDescription: String {
            "command block"
        }
    }

    @Test("decodes command block", arguments: CommandCase.allCases)
    func decode(_ testCase: CommandCase) {
        let result = ClaudeCodeContentDecoder.decode(testCase.input)
        for expected in testCase.expectedContains {
            #expect(result.contains(expected), "Expected '\(expected)' in result")
        }
        for excluded in testCase.expectedExcludes {
            #expect(!result.contains(excluded), "Expected '\(excluded)' to be removed")
        }
    }
}

struct ClaudeCodeCaveatTests {
    @Test("replaces caveat with short label")
    func replaces() {
        let result = ClaudeCodeContentDecoder.decode(DecoderFixtures.caveat)
        #expect(result.contains("[Local command output"))
        #expect(result.contains("do not respond unless explicitly asked"))
        #expect(!result.contains("<local-command-caveat>"))
    }
}

struct ClaudeCodeStdoutTests {
    struct StdoutCase: CustomTestStringConvertible {
        let input: String
        let expectedPrefix: String
        let expectedBody: String

        static let allCases: [StdoutCase] = [
            StdoutCase(
                input: DecoderFixtures.stdoutSingle,
                expectedPrefix: "[stdout]",
                expectedBody: "Hello world"
            ),
            StdoutCase(
                input: DecoderFixtures.stdoutMulti,
                expectedPrefix: "[stdout]",
                expectedBody: "multi"
            ),
        ]

        var testDescription: String {
            "stdout"
        }
    }

    @Test("decodes stdout block", arguments: StdoutCase.allCases)
    func decode(_ testCase: StdoutCase) {
        let result = ClaudeCodeContentDecoder.decode(testCase.input)
        #expect(result.hasPrefix(testCase.expectedPrefix))
        #expect(result.contains(testCase.expectedBody))
    }
}

struct ClaudeCodeTaskNotificationTests {
    struct TaskCase: CustomTestStringConvertible {
        let input: String
        let expectedSummary: String?
        let expectedResult: String?

        static let allCases: [TaskCase] = [
            TaskCase(
                input: DecoderFixtures.taskNotificationFull,
                expectedSummary: "Route calculation",
                expectedResult: "13 segments processed"
            ),
            TaskCase(
                input: DecoderFixtures.taskNotificationSummaryOnly,
                expectedSummary: "Done",
                expectedResult: nil
            ),
            TaskCase(
                input: DecoderFixtures.taskNotificationEmpty,
                expectedSummary: nil,
                expectedResult: nil
            ),
        ]

        var testDescription: String {
            "task notification"
        }
    }

    @Test("decodes task notification", arguments: TaskCase.allCases)
    func decode(_ testCase: TaskCase) {
        let result = ClaudeCodeContentDecoder.decode(testCase.input)
        #expect(!result.contains("<task-notification>"))
        if testCase.expectedSummary == nil, testCase.expectedResult == nil {
            #expect(result == "[Subagent notification]")
        } else {
            #expect(result.contains("[Subagent]"))
            if let expectedSummary = testCase.expectedSummary { #expect(result.contains(expectedSummary)) }
            if let expectedResult = testCase.expectedResult { #expect(result.contains(expectedResult)) }
        }
    }
}

struct ClaudeCodeUnchangedTests {
    struct UnchangedCase: CustomTestStringConvertible {
        let input: String

        static let allCases: [UnchangedCase] = [
            UnchangedCase(input: DecoderFixtures.unchangedGenerics),
            UnchangedCase(input: DecoderFixtures.unchangedCode),
            UnchangedCase(input: DecoderFixtures.unchangedHtml),
            UnchangedCase(input: DecoderFixtures.unchangedPlain),
            UnchangedCase(input: ""),
        ]
        var testDescription: String {
            "unchanged"
        }
    }

    @Test("leaves unknown content unchanged", arguments: UnchangedCase.allCases)
    func unchanged(_ testCase: UnchangedCase) {
        let result = ClaudeCodeContentDecoder.decode(testCase.input)
        #expect(result == testCase.input)
    }
}

struct ClaudeCodeIntegrationTests {
    @Test("decodes mixed content preserving non-Claude XML")
    func mixedContent() {
        let input = """
        User said: fix this
        <command-name>/bash</command-name>
        <command-args>ls -la</command-args>
        And then <String> generic type.
        """
        let result = ClaudeCodeContentDecoder.decode(input)
        #expect(result.contains("[Command]"))
        #expect(result.contains("/bash"))
        #expect(result.contains("ls -la"))
        #expect(result.contains("<String>"), "Swift generics should remain")
        #expect(result.contains("User said: fix this"))
    }

    @Test("empty string returns empty")
    func emptyString() {
        #expect(ClaudeCodeContentDecoder.decode("").isEmpty)
    }
}
